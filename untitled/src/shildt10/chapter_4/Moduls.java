package shildt10.chapter_4;

public class Moduls{
    public static void main(String[] args) {
        int x = 42;
        double d = 42.25;

        System.out.println("x mod 10 = " + x % 10 );
        System.out.println("x mod 10 = " + x % 10 );
        System.out.println("x mod 1 = " + x % 1 );
        System.out.println("d mod 1 = " + d % 1 );
        System.out.println("d mod 10 = " + d % 10 );
        System.out.println("d mod 100 = " + d % 100 );

    }
}
